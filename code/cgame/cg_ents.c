/*
======================
CG_AddRefEntityWithMinLight
======================
*/
void CG_AddRefEntityWithMinLight( const refEntity_t *entity ) {
	refEntity_t re;

	if ( !entity ) {
		return;
	}

	re = *entity;

	// give everything a minimum light add
	re.ambientLight[0] = 32;
	re.ambientLight[1] = 32;
	re.ambientLight[2] = 32;

	trap_R_AddRefEntityToScene( &re );
}

/*
======================
CG_PositionEntityOnTag

Modifies the entities position and axis by the given
tag location
======================
*/
qboolean CG_PositionEntityOnTag( refEntity_t *entity, const refEntity_t *parent,
							qhandle_t parentModel, char *tagName ) {
	int				i;
	orientation_t	lerped;
	qboolean		returnValue;
	
	// lerp the tag
	returnValue = trap_R_LerpTagFrameModel( &lerped, parentModel,
		parent->oldframeModel, parent->oldframe,
		parent->frameModel, parent->frame,
		1.0 - parent->backlerp, tagName );

	// FIXME: allow origin offsets along tag?
	VectorCopy( parent->origin, entity->origin );
	for ( i = 0 ; i < 3 ; i++ ) {
		VectorMA( entity->origin, lerped.origin[i], parent->axis[i], entity->origin );
	}

	// had to cast away the const to avoid compiler problems...
	MatrixMultiply( lerped.axis, ((refEntity_t *)parent)->axis, entity->axis );
	entity->backlerp = parent->backlerp;

	return returnValue;
}


/*
======================
CG_PositionRotatedEntityOnTag

Modifies the entities position and axis by the given
tag location
======================
*/
qboolean CG_PositionRotatedEntityOnTag( refEntity_t *entity, const refEntity_t *parent,
							qhandle_t parentModel, char *tagName ) {
	int				i;
	orientation_t	lerped;
	vec3_t			tempAxis[3];
	qboolean		returnValue;

//AxisClear( entity->axis );
	// lerp the tag
	returnValue = trap_R_LerpTagFrameModel( &lerped, parentModel,
		parent->oldframeModel, parent->oldframe,
		parent->frameModel, parent->frame,
		1.0 - parent->backlerp, tagName );

	// FIXME: allow origin offsets along tag?
	VectorCopy( parent->origin, entity->origin );
	for ( i = 0 ; i < 3 ; i++ ) {
		VectorMA( entity->origin, lerped.origin[i], parent->axis[i], entity->origin );
	}

	// had to cast away the const to avoid compiler problems...
	MatrixMultiply( entity->axis, lerped.axis, tempAxis );
	MatrixMultiply( tempAxis, ((refEntity_t *)parent)->axis, entity->axis );

	return returnValue;
}



